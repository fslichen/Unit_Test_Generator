package generator.codeWriter.pojo;

public class AnnotationArgument {
	private String code;
	private Class<?> clazz;
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public Class<?> getClazz() {
		return clazz;
	}
	public void setClazz(Class<?> clazz) {
		this.clazz = clazz;
	}
}
